<h3>Mallets</h3>
<p>It’s a solid bamboo cane with a hard wood head. It’s about 4 1/2 feet long and you hit a baseball-sized ball made of solid plastic with the side of the mallet, not the end. All players must hold the mallet in the right hand; left-handed play is not allowed.</p>

<h3>The Ball</h3>
<p>Today’s polo ball is a baseball-sized ball made of solid plastic. Historically polo balls were made out of wood.</p>

<h3>The Horse</h3>
<p>They are called polo ponies. Most are former race horses, high-speed Thoroughbreds with exceptional abilities. The tail is braided to stay out of the way of the mallet. The mane is clipped off so the player can see the ball, and to prevent the reins from getting tangled up. The idea is for each player to have six ponies - one for each chukker.</p>

<h3>Horse Equipment</h3>
<p>Polo horses are required to wear a bridle, an English saddle, and their legs are wrapped for support and added protection.</p>
